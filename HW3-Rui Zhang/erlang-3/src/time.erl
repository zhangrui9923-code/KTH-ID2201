-module(time).
-export([zero/0, inc/2, merge/2, leq/2, clock/1, update/3, safe/2]).

%% ============================================================================
%% Lamport时钟基本操作
%% ============================================================================

%% 返回初始Lamport时间值
zero() ->
  0.

%% 增加时钟值
%% Name: 节点名称（为后续扩展保留，当前版本忽略）
%% T: 当前时间值
%% 返回：T + 1
inc(_Name, T) ->
  T + 1.

%% 合并两个Lamport时间戳（取最大值）
%% Ti, Tj: 两个时间戳
%% 返回：max(Ti, Tj)
merge(Ti, Tj) ->
  max(Ti, Tj).

%% 比较两个时间戳
%% Ti, Tj: 两个时间戳
%% 返回：Ti =< Tj
leq(Ti, Tj) ->
  Ti =< Tj.

%% ============================================================================
%% Logger时钟管理（用于holdback队列）
%% ============================================================================

%% 创建一个时钟来跟踪多个节点
%% Nodes: 节点列表
%% 返回：一个映射表，每个节点映射到其最后已知的时间戳
clock(Nodes) ->
  maps:from_list([{Node, zero()} || Node <- Nodes]).

%% 更新时钟
%% Node: 发送消息的节点
%% Time: 消息的时间戳
%% Clock: 当前时钟状态
%% 返回：更新后的时钟
update(Node, Time, Clock) ->
  maps:put(Node, Time, Clock).

%% 判断某个时间的事件是否可以安全打印
%% Time: 事件的时间戳
%% Clock: 当前时钟状态（记录每个节点的最新时间）
%% 返回：true如果安全，false如果还需要等待
safe(Time, Clock) ->
  case maps:values(Clock) of
    [] ->
      true;
    Times ->
      MinTime = lists:min(Times),
      leq(Time, MinTime)
  end.